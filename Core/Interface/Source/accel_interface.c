static bool isEnabled = false;

/*------------------------------------------------------------------------------
 * Local function declarations
 *----------------------------------------------------------------------------*/

/*------------------------------------------------------------------------------
 * Global function definitions
 *----------------------------------------------------------------------------*/

//------------------------------------------------------------------------------
void accelInit(void)
{
    accelDriverInit();
    isEnabled = true;
}

//------------------------------------------------------------------------------
accel_error_e accelRead(accel_reading_t* reading)
{
    if (!isEnabled)
    {
        return ACCEL_ERROR_NOT_ENABLED;
    }

    return accelDriverRead(reading);
}

