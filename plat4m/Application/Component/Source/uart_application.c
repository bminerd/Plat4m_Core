//------------------------------------------------------------------------------
extern void uartApplicationInit(void)
{
    uart_id_map_t idMaps[] =
    {
        // UART_ID_COM_PORT
        {
            .id         = UART_ID_COM_PORT,
            .driverId   = UART_DRIVER_ID_3
        }
    };

    // Add UARTs to interface
    uartAddIdMaps(idMaps, ARRAY_SIZE(idMaps));

    uartSetEnabled(UART_ID_COM_PORT, true);
}

